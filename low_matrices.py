        "def generateChunks(k: int):\n",
        "    final = []\n",
        "    base = np.eye(k)[::-1]  # antidiagonal identity\n",
        "    bases = [base * i for i in range(0, k + 1)]\n",
        "\n",
        "    # Count positions above antidiagonal\n",
        "    fill_positions = [(i, j) for i in range(k) for j in range(k) if i + j < k - 1]\n",
        "    combos = list(product(range(k + 1), repeat=len(fill_positions)))\n",
        "\n",
        "    for b in bases:\n",
        "        for c in combos:\n",
        "            mat = np.zeros((k, k), dtype=int)\n",
        "            for idx, (i, j) in enumerate(fill_positions):\n",
        "                mat[i, j] = c[idx]\n",
        "            mat = mat + b\n",
        "            final.append(mat)\n",
        "    return final\n",
        "\n",
        "def all_kxk_matrices(k: int):\n",
        "    vals = range(k + 1)\n",
        "    for combo in product(vals, repeat=k * k):\n",
