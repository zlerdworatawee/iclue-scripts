        "def bracketing(vec1, vec2):\n",
        "    \"\"\"\n",
        "    Given two vectors (row or column), stack them and generate a flat bracket list:\n",
        "    - ']' for counts from vec1 (first)\n",
        "    - '[' for counts from vec2 (second)\n",
        "    \"\"\"\n",
        "    brackets = []\n",
        "\n",
        "    if vec1.ndim == 1:\n",
        "        vec1 = vec1.reshape(1, -1)  # treat as row\n",
        "    if vec2.ndim == 1:\n",
        "        vec2 = vec2.reshape(1, -1)  # treat as row\n",
        "\n",
        "    if vec1.shape[0] == 1 and vec2.shape[0] == 1:\n",
        "        # row vectors\n",
        "        m_slice = np.vstack([vec1, vec2])\n",
        "        for j in range(m_slice.shape[1]):\n",
        "            brackets.extend([']'] * int(m_slice[0, j]))\n",
        "            brackets.extend(['['] * int(m_slice[1, j]))\n",
        "    elif vec1.shape[1] == 1 and vec2.shape[1] == 1:\n",
        "        # column vectors\n",
        "        m_slice = np.hstack([vec1, vec2])\n",
        "        for i in range(m_slice.shape[0]):\n",
        "            brackets.extend([']'] * int(m_slice[i, 0]))\n",
        "            brackets.extend(['['] * int(m_slice[i, 1]))\n",
        "    return brackets\n"
