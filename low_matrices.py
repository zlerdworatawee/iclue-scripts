        "def clean_brackets(row1, row2):\n",
        "    brackets = bracketing(row1, row2)\n",
        "    brackets = np.array(brackets)\n",
        "\n",
        "    while True:\n",
        "        if np.count_nonzero(brackets == '[') == 0:\n",
        "          return True, brackets\n",
        "        original = brackets.copy()\n",
        "\n",
        "        # Step 1: Remove consecutive ']' to the left of first '[' (if any)\n",
        "        left_mask = np.where(brackets == '[')[0]\n",
        "        if len(left_mask) > 0:\n",
        "            first_open_idx = left_mask[0]\n",
        "            if first_open_idx > 0 and np.all(brackets[:first_open_idx] == ']'):\n",
        "                brackets = brackets[first_open_idx:]\n",
        "        # Step 2: Remove consecutive '[' to the right of last ']' (if any)\n",
        "        right_mask = np.where(brackets == ']')[0]\n",
        "        if len(right_mask) > 0:\n",
        "            last_close_idx = right_mask[-1]\n",
        "            if last_close_idx < len(brackets) - 1 and np.all(brackets[last_close_idx+1:] == '['):\n",
        "                brackets = brackets[:last_close_idx+1]\n",
        "        # Step 3: Remove '[]' pairs from inside out\n",
        "        i = 0\n",
        "        while i < len(brackets) - 1:\n",
        "            if brackets[i] == '[' and brackets[i+1] == ']':\n",
        "                brackets = np.delete(brackets, [i, i+1])\n",
        "                break\n",
        "            else:\n",
        "                i += 1\n",
        "        # Stop if no changes\n",
        "        if np.array_equal(brackets, original):\n",
        "            break\n",
        "\n",
        "    return False, brackets\n"
